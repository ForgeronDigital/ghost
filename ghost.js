var ghost =[
    '-------------------------------------------------------------------------------------------------------------------------------',
    '------oooo---------------------------------------------------------------------------------------------------------------------',
    '----oooooooo-------------------------------------------------------------------------------------------------------------------',
    '---oooooooooo------------------------------------------------------------------------------------------------------------------',
    '--ooo--oooo--o-----------------------------------------------------------------------------------------------------------------',
    '--oo----oo---------------------------------------------------------------------------------------------------------------------',
    '--oo--88oo--88-----------------------------------------------------------------------------------------------------------------',
    '-ooo--88oo--88o----------------------------------------------------------------------------------------------------------------',
    '-oooo--oooo--oo----------------------------------------------------------------------------------------------------------------',
    '-oooooooooooooo----------------------------------------------------------------------------------------------------------------',
    '-oooooooooooooo----------------------------------------------------------------------------------------------------------------',
    '-oooooooooooooo----------------------------------------------------------------------------------------------------------------',
    '-oooooooooooooo----------------------------------------------------------------------------------------------------------------',
    '-oo-ooo--ooo-oo----------------------------------------------------------------------------------------------------------------',
    '-o---oo--oo---o----------------------------------------------------------------------------------------------------------------',
    '-------------------------------------------------------------------------------------------------------------------------------',
];
var types ={
    '-': 'empty',
    'x': 'plain',
    'o': 'light',
    '8': 'highlight',
};
var afficher = function() {
    document.getElementById('container').innerHTML="";
for (const key in ghost) {
    var x = ghost[key];
    for (const key1 in x) {
        var pixel = ghost[key][key1];
        var pixelElts=document.createElement('div');  
        pixelElts.classList.add(types[pixel]);
        document.getElementById('container').appendChild(pixelElts);     
    }
}
document.getElementById('container').style.width=(ghost[0].length * 10) + 'px';}

var defiler = function(){
    for (const key in ghost) {
        var x = ghost[key];
        var lastIndex =x[x.length-1];
        ghost[key]=ghost[key].slice(0,x.length-1);
        ghost[key]=lastIndex.concat(ghost[key]);
    }
}
setInterval(afficher,66);
setInterval(defiler,66);